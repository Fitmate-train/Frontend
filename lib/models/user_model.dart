class User {
  String name;
  String gender;
  int age;
  double height;
  double weight;
  String bodyType;
  String goal;
  String preferredTime;
  String preferredStyle;

  User({
    required this.name,
    required this.gender,
    required this.age,
    required this.height,
    required this.weight,
    required this.bodyType,
    required this.goal,
    required this.preferredTime,
    required this.preferredStyle,
  });
}
